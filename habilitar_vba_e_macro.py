def habilitar_vba_office():
    """
    Habilita VBA e macros no Office via registro do Windows
    """
    print("üîß Configurando acesso VBA e MACROS no Office...")
    print("=" * 50)
    
    # Verificar privil√©gios
    admin_status = "‚úÖ ADMINISTRADOR" if is_admin() else "‚ö†Ô∏è USU√ÅRIO COMUM"
    print(f"üîê Status: {admin_status}")
    
    # Vers√µes do Office (incluindo vers√µes mais recentes)
    office_versions = {
        "16.0": "Office 2016/2019/2021/365",
        "15.0": "Office 2013", 
        "14.0": "Office 2010",
        "12.0": "Office 2007"
    }
    
    # Aplica√ß√µes para configurar
    applications = ["Word", "Excel", "PowerPoint", "Access"]
    
    success_count = 0
    total_attempts = 0
    
    for version, version_name in office_versions.items():
        print(f"\nüìã Configurando {version_name} ({version})...")
        
        for app in applications:
            total_attempts += 1
            
            # Configurar HKEY_CURRENT_USER (sempre funciona)
            with registry_key(winreg.HKEY_CURRENT_USER, 
                            f"SOFTWARE\\Microsoft\\Office\\{version}\\{app}\\Security") as key:
                if key:
                    try:
                        # Configura√ß√µes de seguran√ßa VBA
                        winreg.SetValueEx(key, "AccessVBOM", 0, winreg.REG_DWORD, 1)
                        winreg.SetValueEx(key, "VBAWarnings", 0, winreg.REG_DWORD, 1)
                        winreg.SetValueEx(key, "Level", 0, winreg.REG_DWORD, 1)
                        
                        # Configura√ß√µes adicionais para maior compatibilidade
                        winreg.SetValueEx(key, "ExtensionHardening", 0, winreg.REG_DWORD, 0)
                        
                        print(f"  ‚úÖ {app} - Configura√ß√µes aplicadas (usu√°rio atual)")
                        success_count += 1
                        
                    except Exception as e:
                        print(f"  ‚ùå {app} - Erro ao configurar: {e}")
            
            # Tentar configurar HKEY_LOCAL_MACHINE (requer admin)
            if is_admin():
                with registry_key(winreg.HKEY_LOCAL_MACHINE, 
                                f"SOFTWARE\\Microsoft\\Office\\{version}\\{app}\\Security") as key:
                    if key:
                        try:
                            winreg.SetValueEx(key, "AccessVBOM", 0, winreg.REG_DWORD, 1)
                            winreg.SetValueEx(key, "VBAWarnings", 0, winreg.REG_DWORD, 1)
                            winreg.SetValueEx(key, "Level", 0, winreg.REG_DWORD, 1)
                            winreg.SetValueEx(key, "ExtensionHardening", 0, winreg.REG_DWORD, 0)
                            print(f"  üîê {app} - Configura√ß√£o global aplicada")
                        except Exception as e:
                            print(f"  ‚ö†Ô∏è {app} - Erro na configura√ß√£o global: {e}")
    
    # Verificar configura√ß√µes aplicadas
    print(f"\nüîç Verificando configura√ß√µes aplicadas...")
    verificar_configuracoes_silencioso()
    
    # Resultado final
    print("\n" + "=" * 50)
    print("üéØ Configura√ß√£o conclu√≠da!")
    print(f"‚úÖ {success_count}/{total_attempts} configura√ß√µes aplicadas")
    
    if success_count > 0:
        print("\n‚ö†Ô∏è IMPORTANTE - PR√ìXIMOS PASSOS:")
        print("   1. Feche TODOS os programas do Office abertos")
        print("   2. Abra o Word/Excel novamente") 
